package javaStudy;

public class primitiveDataTypeExam {

	public static void main(String[] arg) {
		boolean isFun = true;
//		boolean은 true 또는 fulse 두개 값만 가능
		System.out.println(isFun);
		
		char c = 'f';
//		문자 하나만 가능
		int x = 59;
		
		long bing = 34343434343l;
//		좀 더 큰 정수 사용시 , 숫자뒤에 l 꼭 삽입
		
		float f = 32.4f;
//		숫자뒤에 f 꼭 삽입
		double d = 3434343434.5;
//		 좀 더 큰 실수 사용시
		
	}
}
